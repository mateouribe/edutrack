let User = function (
  role,
  fullName,
  dateOfBirth,
  phoneNumber,
  address,
  zipCode,
  username,
  password
) {
  this.role = role;
  this.fullName = fullName;
  this.dateOfBirth = dateOfBirth;
  this.phoneNumber = phoneNumber;
  this.address = address;
  this.zipCode = zipCode;
  this.username = username;
  this.password = password;
};
